saldo = 600
saque = 600

print(saldo == saque) # comparação de igualdade
print(saldo != saque) # comparação de diferença
print(saldo > saque) # comparação de maior que
print(saldo < saque) # comparação de menor que
print(saldo >= saque) # comparação de maior ou igual a
print(saldo <= saque) # comparação de menor ou igual a
